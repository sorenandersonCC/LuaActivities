--[[
   Turtle Dancer: Using while loops 
   create a program that makes your
   turtle dance forever. (dance)

   Purpose: To help the kids understand 
   what a while loop actually looks like
   and how it works in the “real world”.

   It also introduces the basics to 
   understand all the other loops and 
   how they work as well as using break.
]]--

while true do
   turtle.up()
   turtle.down()
   turtle.turnLeft()
   turtle.turnLeft()
   turtle.forward()
   turtle.back()
   turtle.turnLeft()
   turtle.turnLeft()
end